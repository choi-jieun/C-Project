#include <stdio.h>

#define SIZE 5  // 큐 크기 정의

// 큐 자료형 정의
typedef struct {
    int data[SIZE];  // 데이터를 저장할 배열
    int front;       // 가장 앞쪽 요소의 인덱스 (제거 대상)
    int rear;        // 가장 마지막 요소의 인덱스 (추가 대상)
} Queue;

// 큐 초기화 함수
void initQueue(Queue* q) {
    q->front = -1;
    q->rear = -1;
}


// 큐가 비었는지 확인
int isEmpty(Queue* q) {
    return q->front == q->rear;
}

// 큐가 가득 찼는지 확인
int isFull(Queue* q) {
    return q->rear == SIZE - 1;
}

// 큐에 요소를 추가하는 함수 (enqueue)
void enqueue(Queue* q, int value) {
    if (isFull(q)) {
        printf("큐가 가득 찼습니다. 더 이상 추가할 수 없습니다.\n");
        return;
    }
    q->rear++;
    q->data[q->rear] = value;
}

// 큐에서 요소를 제거하고 반환하는 함수 (dequeue)
int dequeue(Queue* q) {
    if (isEmpty(q)) {
        printf("큐가 비어 있습니다. 제거할 수 없습니다.\n");
        return -1;
    }
    q->front++;
    return q->data[q->front];
}




// 메인 함수
int main() {
    Queue q;
    initQueue(&q);  // 큐 초기화

    int cards[SIZE] = { 3, 7, 2, 9, 5 };  // 카드 번호 입력 (고정된 5장)

    // 카드를 큐에 저장 (enqueue)
    for (int i = 0; i < SIZE; i++) {
        enqueue(&q, cards[i]);
    }

    // 큐에서 카드를 하나씩 꺼내며 출력 (dequeue)
    printf("카드 공개 순서: ");
    for (int i = 0; i < SIZE; i++) {
        int card = dequeue(&q);  // 앞에서부터 하나씩 꺼냄
        printf("%d ", card);
    }

    printf("\n");
    return 0;
}
 